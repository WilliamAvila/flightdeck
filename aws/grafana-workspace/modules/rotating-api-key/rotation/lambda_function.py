def finish_secret(service_client, arn, token):
    """Finish the secret

    This method finalizes the rotation process by marking the secret version
    passed in as the AWSCURRENT secret.

    Args:
        service_client (client): The secrets manager service client

        arn (string): The secret ARN or other identifier

        token (string): The ClientRequestToken associated with the secret version

    Raises:
        ResourceNotFoundException: If the secret with the specified arn does not exist

    """
    # First describe the secret to get the current version
    metadata = service_client.describe_secret(SecretId=arn)
    current_version = None
    for version in metadata["VersionIdsToStages"]:
        if "AWSCURRENT" in metadata["VersionIdsToStages"][version]:
            if version == token:
                # The correct version is already marked as current, return
                logger.info("finishSecret: Version %s already marked as AWSCURRENT for %s" % (version, arn))
                return
            current_version = version
            break

    # Finalize by staging the secret version current
    service_client.update_secret_version_stage(SecretId=arn, VersionStage="AWSCURRENT", MoveToVersionId=token, RemoveFromVersionId=current_version)
    logger.info("finishSecret: Successfully set AWSCURRENT stage to version %s for secret %s." % (token, arn))

def get_secret_dict(service_client, arn, stage, token=None):
    """Gets the secret dictionary corresponding for the secret arn, stage, and token
    This helper function gets credentials for the arn and stage passed in and returns the dictionary by parsing the JSON string
    Args:
        service_client (client): The secrets manager service client
        arn (string): The secret ARN or other identifier
        token (string): The ClientRequestToken associated with the secret version, or None if no validation is desired
        stage (string): The stage identifying the secret version
    Returns:
        SecretDictionary: Secret dictionary
    Raises:
        ResourceNotFoundException: If the secret with the specified arn and stage does not exist
        ValueError: If the secret is not valid JSON
        KeyError: If the secret json does not contain the expected keys
    """
    required_fields = ['GRAFANA_API_KEY']

    # Only do VersionId validation against the stage if a token is passed in
    if token:
        secret = service_client.get_secret_value(SecretId=arn, VersionId=token, VersionStage=stage)
    else:
        secret = service_client.get_secret_value(SecretId=arn, VersionStage=stage)
    plaintext = secret['SecretString']
    secret_dict = json.loads(plaintext)

    for field in required_fields:
        if field not in secret_dict:
            raise KeyError("%s key is missing from secret JSON" % field)

    # Parse and return the secret JSON string
    return secret_dict

def check_grafana_api_key(secret):
    expected_key_name = secret['GRAFANA_KEY_NAME']
    existing = get_grafana_api_key(secret, expected_key_name)
    if existing == None:
        raise ValueError('No key named %s' % (expected_key_name))
    logger.info("Found expected API key %s" % (expected_key_name))

def create_grafana_api_key(secret, create_key_name):
    body = json.dumps({
        'name': create_key_name,
        'role':'Admin',
        'secondsToLive':2592000
    })
    response =  grafana_request(secret, "POST", "/api/auth/keys", body)
    logger.info("Created API key %s" % (create_key_name))
    return response

def get_grafana_api_key(secret, expected_key_name):
    keys = grafana_request(secret, "GET", "/api/auth/keys")
    matching = [key for key in keys if key['name'] == expected_key_name]
    if matching == []:
        return None
    else:
        return matching[0]

def grafana_request(secret, method, path, body=None):
    api_key = secret['GRAFANA_API_KEY']
    api_root = urllib.parse.urljoin(WORKSPACE_URL, path)
    uri = urllib.parse.urlparse(api_root)
    connection = http.client.HTTPSConnection(uri.netloc, 443, timeout=10)
    headers = { "Authorization": "Bearer %s" % api_key, "Content-Type": "application/json" }
    connection.request(method, uri.path, body=body, headers=headers)
    response = connection.getresponse()
    body = response.read()
    connection.close()
    if body == b'':
        return None

    try:
        return json.loads(body)
    except json.decoder.JSONDecodeError:
        raise ValueError('Unable to decode response: %s' % body)
